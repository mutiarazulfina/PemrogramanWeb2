<?php

//Menetapkan Nilai
$n = 10;
echo "Aku adalah angka $n </br>";

//Perkalian
$hasilkali = $n * 8; //Tanda * adalah operator
echo "Jika aku dikali 8, jumlahku sekarang 80 = $hasilkali </br>";

//Pembagian
$hasilbagi = $hasilkali / 4; //Tanda / adalah operator
echo "Jika aku dibagi 4, jumlahku sekarang 20 = $hasilbagi </br>";

//Pengurangan
$hasilkurang = $hasilbagi - 6; //Tanda - adalah operator
echo "Jika aku dikurang 6, jumlahku sekarang 14 = $hasilkurang </br>";

//Penambahan
$hasiltambah = $hasilkurang + 2; //Tanda + adalah operator
echo "Jika aku ditambah 2, jumlahku sekarang 12 = $hasiltambah </br>";

?>